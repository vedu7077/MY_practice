package com.arrays;

public class OddNumber {

	public static void main(String[] args) {
		
		int a []= {77,88,99,55,44,11};
		
		
		
		for(int number: a) {
			
			if (number% 2 !=0) {
				
				System.out.println(number +"This Is Odd Number :");
			}	
				else {
					
					System.out.println(number +"This Is Even Number ");
				}
				
			}
		}
	
		
		
		
		

	}
